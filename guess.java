    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        int ri;
       
        switch(rn){
            case 0:
                jTextPane1.setText(def);
                break;
            case 1:
                 String[] one={"its a first odd number","its a smallest possible positive number","it's tan 45 degree value"};
                ri=rno.nextInt(one.length);
                jTextPane1.setText(one[ri]);
                break;
            case 2:
                String[] two={"it's a first even number","it's smallest prime number","it is the only even prime number"};
                ri=rno.nextInt(two.length);
                jTextPane1.setText(two[ri]);
                break;
            case 3:
                String[] three={"it's floor(pi) value","in binary representation,it's written as 11","it is second  prime number"};
                ri=rno.nextInt(three.length);
                jTextPane1.setText(three[ri]);
                break;
            case 4:
                 String[] four={"it is the square of 2","seasons in a year","it is second  prime number"};
                ri=rno.nextInt(four.length);
                jTextPane1.setText(four[ri]);
                break;
            default:
                if(rn%2==0){
                    if(rn%2==0){
                        jTextPane1.setText("\n\n\n   it is a even number");
                    }
                    else{
                        jTextPane1.setText("\n\n\n   it is a odd number");
                    }     
                }
                else{
                String binary=Integer.toBinaryString(rn);
                jTextPane1.setText("\n\n\n in binary representation,it's written as '"+binary+"'");
                break;
                }
        }    
        
   
    }                                        

