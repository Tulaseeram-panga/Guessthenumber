public static String find(int gn,int rn){
  if(gn==rn){
      return "Congratulations! you're guessed the number in "+count+" attempts";
  }
  else if(gn<rn){
      return "   Try a higher number";
  }
  else if(gn>rn){
      return "   Try a lower number";
  }
  
   
     
    return null;  
}
